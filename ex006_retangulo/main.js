/*6) Escreva um algoritmo para ler as dimensões de um retângulo (base e altura), calcular e escrever a
área do retângulo. */

var base = parseInt(prompt("Insira o valor da base de um retângulo:"));
var altura = parseInt(prompt("Insira o valor da altura de um retângulo:"));
var area = base * altura

alert("A área desse retângulo mede " + area);